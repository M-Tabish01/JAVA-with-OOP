
package casting;


public class Casting {

    public static void main(String[] args) {
        int P = 10;
        int FP = P + (int)199.98; //Explicit Casting
        System.out.println(FP);
        double EP = P + 1999998899.989898;
        float number = 3.14F;
        // COnstant
        final int integer = 10;
        System.out.println(integer);
    }
    
}
